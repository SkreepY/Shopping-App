const mongoose = require("mongoose");

const schema = mongoose.Schema;

const shoppingSchema = new schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
const shop = mongoose.model("shop", shoppingSchema);
module.exports = shop;
